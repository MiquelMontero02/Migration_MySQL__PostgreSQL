    E.DNI AS DNI,
    E.EMAIL AS EMAIL,
    E.NOM AS NOM,
    E.COGNOM_1 AS COGNOM,
    E.COGNOM_2 AS COGNOM_AD,
    (SELECT 
        M.ID
    FROM GESTMAT.MUNICIPI M 
    WHERE 
        CASE
            WHEN E.MUNICIPI="CIUTADELLA" THEN M.NOM LIKE "CIUTADELLA %"
            ELSE M.NOM=E.MUNICIPI
            END LIMIT 1) AS MUNICIPI
FROM RAW_IMPORT.ESTUDIANT E;

/*ADRECA*/

